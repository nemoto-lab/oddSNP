@snpic.command()
@click.argument('inpath')
@click.argument('oupath')
@click.option('--force', is_flag=True, help='Override target files.')
def run_all(inpath, oupath, force):
    """Full SNP-IC calculation pipeline.
  
    Arguments:
    
        INPATH: Path to the folder containing cellSNP output files. The folder
            should contain at least the following files: cellSNP.samples.tsv, 
            cellSNP.tag.AD.mtx, cellSNP.tag.DP.mtx, and cellSNP.tag.OTH.mtx.
        
        OUPATH: Path to the output folder where all results will be stored. 
    """
    # step 1: calculate the SNP-IC counts
    snpicfile = call_calculate_snpic(
      inpath=inpath,
      oupath=oupath,
      force=force
    )
    
    # step 2: generate the SNP-IC histogram
    histofile = call_generate_histogram(
      agfile=snpicfile,
      oupath=oupath,
      force=force
    )

    # step 3: generate the SNP-IC plot
    fig = plotting.generate_snpic_plot(histofile)
    plotting.save_plot(
      figure=fig,
      output='{}/snpic_figure.html'.format(oupath),
      force=force
    )

def call_run_all(inpath, oupath, force):
    """Python API wrapper for :func:`run_all`.
    
    Args:
        inpath: Path to the folder containing cellSNP output files. The folder
            should contain at least the following files: cellSNP.samples.tsv, 
            cellSNP.tag.AD.mtx, cellSNP.tag.DP.mtx, and cellSNP.tag.OTH.mtx.
        oupath: Path to the output folder where all results will be stored.
        force: If ``True``, overrides any existing output files.
    """
    return run_all.callback(
      inpath=inpath,
      oupath=oupath,
      force=force
    )

