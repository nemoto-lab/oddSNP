@snpic.command()
@click.argument('barfile', type=click.Path(exists=True, dir_okay=False))
@click.argument('agfile', type=click.Path(exists=True, dir_okay=False))
@click.argument('oupath', type=str)
@click.argument('n')
@click.option('--seed', type=int, default=None, help='Seed used for reproducible results.')
@click.option('--force', is_flag=True, help='Override target files')
def estimate_donor_assignment(barfile, agfile, oupath, n, seed, force):
    """Estimate donor assignment for each barcode based on SNP-IC values.

    Arguments:

        BARCODES: Path to the list of barcodes to be assigned
    
        AGFILE: Path to the SNP-IC counts file (pkl.gz) generated by calculate_snpic.
        
        OUPATH: Path to the folder where to store the resulting bars file
    
        N: Number of donors (ex: 5) or List of probabilities (ex: [0.1,0.3,0.4,0.2] - no spaces).
    """
    tgt = Path(f"{oupath}/snpic_bars.pkl")
    if tgt.is_file() and not force:
      print(f"SNP-IC donor estimation bars file found at: {tgt}. Skipping...")
      return tgt

    # read the file and store as a list
    bcodes = list(pd.read_table(barfile, names=['bcode']).bcode)
    
    # provide seed for deterministic output
    rng = np.random.default_rng(seed) 
    
    # depending on options provided we proceed slightly different with respect to 
    # the probabilities used for the multimodal distribution used for the donor
    # assignation
    n = ast.literal_eval(str(n))
    assert type(n) in [int, list], "--n should be either an integer or a list of probabilities"
    if type(n) == int:
      print('Using --n equally distributed donors for barcode/donor mapping estimation')
      # number of cells per donor based on equal probabilities
      cpd = rng.multinomial(len(bcodes), [1/n]*n)
    else:
      print('Using --n probabilities for barcode/donor mapping estimation')
      # number of cells per donor
      cpd = rng.multinomial(len(bcodes), n)
    print(cpd)
    
    # the cummulative sum, i.e indices in the bcodes list associated to each donor
    cpdc = np.cumsum(cpd) 

    # randomly shuffle the cells
    rng.shuffle(bcodes)
    # load the barcodes and their associated SNP-IC values
    snpic = pd.read_pickle(agfile)
    snpicbcode = set(snpic.index) #list of bcodes with SNP-IC value available

    snpic['donor'] = -1
    snpic['correct'] = False

    # determine the barcodes associated to each donor and how many of them 
    # can be considered correctly assigned based on the SNP-IC>50 threshold
    j=0
    for idx,i in enumerate(cpdc):
      sel = bcodes[j:i] # selected barcodes
      # index(barcodes) != index(snp-ic barcodes)
      isec = snpicbcode.intersection(set(sel))
      # save donor and correctly assigned
      
      snpic.loc[list(isec),'donor'] = idx
      # print(snpic.loc[list(isec),'donor'])

      snpic.loc[(snpic.donor==idx) & (snpic.DPOTH >= 50), 'correct'] = True
      # print(snpic.loc[(snpic.donor==idx) & (snpic.DPOTH >= 50), 'correct'])
      j = i

    # save the resulting file
    print(f"Saving donor estimation bars to: {tgt}")
    snpic = snpic[['donor', 'correct']]
    Path(oupath).mkdir(parents=True, exist_ok=True)
    snpic.to_pickle(tgt)
    
    # return the file where the bars are stored
    return tgt

